function createBookCard(book) {
    var div = document.createElement('div');
    div.className = 'book-card';

    var wrapper = document.createElement('div');
    wrapper.className = 'book-cover-wrapper';
    wrapper.style.background = 'linear-gradient(145deg, ' + book.color + ', ' + adjustColor(book.color, 30) + ')';

    var img = document.createElement('img');
    img.className = 'book-cover';
    img.alt = book.title;
    img.referrerPolicy = 'no-referrer';

    var fallback = document.createElement('div');
    fallback.className = 'book-cover-fallback';
    fallback.textContent = book.title;

    wrapper.appendChild(img);
    wrapper.appendChild(fallback);

    var info = document.createElement('div');
    info.className = 'book-info';

    var titleEl = document.createElement('div');
    titleEl.className = 'book-title';
    titleEl.textContent = book.title;

    var authorEl = document.createElement('div');
    authorEl.className = 'book-author';
    authorEl.textContent = book.author;

    info.appendChild(titleEl);
    info.appendChild(authorEl);

    div.appendChild(wrapper);
    div.appendChild(info);

    return div;
}

function adjustColor(hex, amount) {
    var num = parseInt(hex.replace('#', ''), 16);
    var r = Math.min(255, Math.max(0, (num >> 16) + amount));
    var g = Math.min(255, Math.max(0, ((num >> 8) & 0x00FF) + amount));
    var b = Math.min(255, Math.max(0, (num & 0x0000FF) + amount));
    return '#' + ((r << 16) | (g << 8) | b).toString(16).padStart(6, '0');
}

function getCoverUrls(book) {
    var urls = [];

    if (book.directCover) {
        urls.push(book.directCover);
    }

    if (book.isbns) {
        book.isbns.forEach(function(isbn) {
            urls.push('https://books.google.com/books/content?vid=ISBN:' + isbn + '&printsec=frontcover&img=1&zoom=1');
        });

        book.isbns.forEach(function(isbn) {
            urls.push('https://covers.openlibrary.org/b/isbn/' + isbn + '-L.jpg');
        });
    }

    return urls;
}

function isGooglePlaceholder(imgEl, url) {
    if (url.indexOf('books.google.com') === -1) return false;
    var w = imgEl.naturalWidth;
    var h = imgEl.naturalHeight;
    // Google Books placeholders: "image not available" is 128x170,
    // gray striped is 128x184. Reject exact matches.
    if (w === 128 && (h === 170 || h === 184)) return true;
    // Also reject very short aspect ratios (wrong book covers like 128x102)
    if (w > 0 && h / w < 1.2) return true;
    return false;
}

function tryLoadCover(img, urls, index) {
    if (index === undefined) index = 0;
    if (index >= urls.length) return;

    var timer = setTimeout(function() {
        img.onload = null;
        img.onerror = null;
        tryLoadCover(img, urls, index + 1);
    }, 5000);

    img.onload = function() {
        clearTimeout(timer);
        if (this.naturalWidth > 50 && this.naturalHeight > 50 && !isGooglePlaceholder(this, urls[index])) {
            this.classList.add('loaded');
        } else {
            tryLoadCover(img, urls, index + 1);
        }
    };

    img.onerror = function() {
        clearTimeout(timer);
        tryLoadCover(img, urls, index + 1);
    };

    img.src = urls[index];
}

// Initialize all year sections
document.querySelectorAll('.year-section').forEach(function(section) {
    var year = section.dataset.year;
    var books = booksByYear[year] || [];
    var grid = section.querySelector('.books-grid');
    var toggle = section.querySelector('.year-toggle');

    // Populate books
    books.forEach(function(book) {
        var card = createBookCard(book);
        grid.appendChild(card);

        if (!book.noImage) {
            var img = card.querySelector('.book-cover');
            var urls = getCoverUrls(book);
            if (urls.length > 0) {
                tryLoadCover(img, urls);
            }
        }
    });

    // Toggle with animated expand/collapse
    toggle.addEventListener('click', function() {
        var isExpanded = this.getAttribute('aria-expanded') === 'true';

        if (isExpanded) {
            // Collapse: set explicit height first so transition has a start value
            grid.style.maxHeight = grid.scrollHeight + 'px';
            grid.offsetHeight; // force reflow
            grid.style.maxHeight = '0';
            grid.style.opacity = '0';
            this.setAttribute('aria-expanded', 'false');
        } else {
            // Expand: transition from 0 to measured height
            grid.offsetHeight; // force reflow
            grid.style.maxHeight = grid.scrollHeight + 'px';
            grid.style.opacity = '1';
            this.setAttribute('aria-expanded', 'true');

            var onEnd = function(e) {
                if (e.propertyName === 'max-height') {
                    grid.style.maxHeight = 'none';
                    grid.removeEventListener('transitionend', onEnd);
                }
            };
            grid.addEventListener('transitionend', onEnd);
        }
    });
});
